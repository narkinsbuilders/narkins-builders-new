/**
 * Utility function to get alt text for an image
 * @param filename - The image filename
 * @param fallback - Optional custom fallback text
 * @param context - Optional context for better SEO (e.g., 'carousel', 'gallery', 'lightbox')
 * @param index - Optional index for dynamic content
 * @returns SEO-optimized alt text
 */
export const getImageAltText = (filename: string, fallback?: string, context?: string, index?: number): string => {
 const seoAltText = imageAltTexts[filename];
 
 if (seoAltText) {
  return seoAltText;
 }
 
 // Handle dynamic context-based alt text
 if (context && index !== undefined) {
  switch (context) {
   case 'carousel':
    return getCarouselAltText(filename, index);
   case 'gallery':
    return getGalleryAltText(filename, index);
   case 'lightbox':
    return getLightboxAltText(filename);
   default:
    break;
  }
 }
 
 if (fallback) {
  return fallback;
 }
 
 // Generate fallback from filename
 const cleanedName = filename
  .replace(/\.(webp|jpg|jpeg|png)$/i, '')
  .replace(/-/g, ' ')
  .replace(/\b\w/g, l => l.toUpperCase());
  
 return `Narkin's Builders - ${cleanedName}`;
};

